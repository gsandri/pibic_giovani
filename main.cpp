double corr(double *v1, double *v2, int N)
{
    double S12 = 0, S11 = 0, S22 = 0;
    double m1 = 0, m2 = 0;

    for(int n=0; n<N; n++)
    {
        m1 += v1[n];
        m2 += v2[n];
    }
    m1 /= N;
    m2 /= N;

    for(int n=0; n<N; n++)
    {
        double d1 = v1[n]-m1;
        double d2 = v2[n]-m2;

        S12 += d1*d2;
        S11 += d1*d1;
        S22 += d2*d2;
    }

    return S12/sqrt(S11*S22);
}

